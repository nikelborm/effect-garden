/**
 * The dist object is generated by npm and may be relied upon. Each dist object has at least two fields:
 * - tarball: the url of the tarball containing the payload for this package
 * - shasum: the SHA-1 sum of the tarball
 * - integrity: since Apr 2017, string in the format `<hashAlgorithm>-<base64-hash>`, refer the Subresource Integrity and cacache package for more
 * - fileCount: since Feb 2018, the number of files in the tarball, folder excluded
 * - unpackedSize: since Feb 2018, the total byte of the unpacked files in the tarball
 * - npm-signature: since Apr 2018, a PGP signature of `<package>@<version>:<integrity>`, refer the npm blog and doc for more (in the future) a SHA-2 512 sum of the tarball
 */
export class DistInfo extends Schema.Class<DistInfo>('DistInfo')({
  /**
   * The url of the tarball containing the payload for this package
   */
  tarball: Schema.String,
  /**
   * The SHA-1 sum of the tarball
   */
  shasum: Schema.String,
  /**
   * Since Apr 2017, string in the format `<hashAlgorithm>-<base64-hash>`, refer the Subresource Integrity and cacache package for more
   */
  integrity: Schema.optionalWith(Schema.String, { nullable: true }),
  /**
   * Since Feb 2018, the number of files in the tarball, folder excluded
   */
  fileCount: Schema.optionalWith(Schema.Int, { nullable: true }),
  /**
   * Since Feb 2018, the total byte of the unpacked files in the tarball
   */
  unpackedSize: Schema.optionalWith(Schema.Int, { nullable: true }),
  /**
   * PGP signature of `<package>@<version>:<integrity>`
   */
  'npm-signature': Schema.optionalWith(Schema.String, { nullable: true }),
  signatures: Schema.optionalWith(
    Schema.Array(
      Schema.Struct({
        keyid: Schema.String,
        sig: Schema.String,
      }),
    ),
    { nullable: true },
  ),
}) {}

