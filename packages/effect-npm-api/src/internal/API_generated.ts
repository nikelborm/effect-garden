export const make = (
  httpClient: HttpClient.HttpClient,
  options: {
    readonly transformClient?:
      | ((
          client: HttpClient.HttpClient,
        ) => Effect.Effect<HttpClient.HttpClient>)
      | undefined
  } = {},
): NPM => {
  const unexpectedStatus = (response: HttpClientResponse.HttpClientResponse) =>
    Effect.flatMap(
      Effect.orElseSucceed(response.json, () => 'Unexpected status code'),
      description =>
        Effect.fail(
          new HttpClientError.ResponseError({
            request: response.request,
            response,
            reason: 'StatusCode',
            description:
              typeof description === 'string'
                ? description
                : JSON.stringify(description),
          }),
        ),
    )
  const withResponse: <A, E>(
    f: (response: HttpClientResponse.HttpClientResponse) => Effect.Effect<A, E>,
  ) => (
    request: HttpClientRequest.HttpClientRequest,
  ) => Effect.Effect<any, any> = options.transformClient
    ? f => request =>
        Effect.flatMap(
          Effect.flatMap(options.transformClient!(httpClient), client =>
            client.execute(request),
          ),
          f,
        )
    : f => request => Effect.flatMap(httpClient.execute(request), f)
  const decodeSuccess =
    <A, I, R>(schema: Schema.Schema<A, I, R>) =>
    (response: HttpClientResponse.HttpClientResponse) =>
      HttpClientResponse.schemaBodyJson(schema)(response)
  const decodeError =
    <const Tag extends string, A, I, R>(
      tag: Tag,
      schema: Schema.Schema<A, I, R>,
    ) =>
    (response: HttpClientResponse.HttpClientResponse) =>
      Effect.flatMap(
        HttpClientResponse.schemaBodyJson(schema)(response),
        cause => Effect.fail(NPMError(tag, cause, response)),
      )
  return {
    httpClient,
    'GET/': () =>
      HttpClientRequest.get(`/`).pipe(
        withResponse(
          HttpClientResponse.matchStatus({
            '2xx': decodeSuccess(RegistryMetadata),
            orElse: unexpectedStatus,
          }),
        ),
      ),
    'GET/-/all': () =>
      HttpClientRequest.get(`/-/all`).pipe(
        withResponse(
          HttpClientResponse.matchStatus({
            '2xx': decodeSuccess(All200),
            '404': decodeError('NpmError', NpmError),
            orElse: unexpectedStatus,
          }),
        ),
      ),
    'GET/{packageName}': packageName =>
      HttpClientRequest.get(`/${packageName}`).pipe(
        withResponse(
          HttpClientResponse.matchStatus({
            '2xx': decodeSuccess(PackageMetadata),
            '404': decodeError('NpmError', NpmError),
            orElse: unexpectedStatus,
          }),
        ),
      ),
    'GET/{packageName}/{version}': (packageName, version) =>
      HttpClientRequest.get(`/${packageName}/${version}`).pipe(
        withResponse(
          HttpClientResponse.matchStatus({
            '2xx': decodeSuccess(PackageVersionMetadata),
            '404': decodeError('NpmError', NpmError),
            orElse: unexpectedStatus,
          }),
        ),
      ),
    'GET/-/v1/search': options =>
      HttpClientRequest.get(`/-/v1/search`).pipe(
        HttpClientRequest.setUrlParams({
          text: options?.['text'] as any,
          size: options?.['size'] as any,
          from: options?.['from'] as any,
          quality: options?.['quality'] as any,
          popularity: options?.['popularity'] as any,
          maintenance: options?.['maintenance'] as any,
        }),
        withResponse(
          HttpClientResponse.matchStatus({
            '2xx': decodeSuccess(SearchResults),
            orElse: unexpectedStatus,
          }),
        ),
      ),
    'GET/downloads/point/{period}': period =>
      HttpClientRequest.get(`/downloads/point/${period}`).pipe(
        withResponse(
          HttpClientResponse.matchStatus({
            '2xx': decodeSuccess(RegistryDownloads),
            orElse: unexpectedStatus,
          }),
        ),
      ),
    'GET/downloads/point/{period}/{packageName}': (period, packageName) =>
      HttpClientRequest.get(`/downloads/point/${period}/${packageName}`).pipe(
        withResponse(
          HttpClientResponse.matchStatus({
            '2xx': decodeSuccess(PackageDownloads),
            orElse: unexpectedStatus,
          }),
        ),
      ),
    'GET/downloads/range/{period}': period =>
      HttpClientRequest.get(`/downloads/range/${period}`).pipe(
        withResponse(
          HttpClientResponse.matchStatus({
            '2xx': decodeSuccess(DailyRegistryDownloads),
            orElse: unexpectedStatus,
          }),
        ),
      ),
    'GET/downloads/range/{period}/{packageName}': (period, packageName) =>
      HttpClientRequest.get(`/downloads/range/${period}/${packageName}`).pipe(
        withResponse(
          HttpClientResponse.matchStatus({
            '2xx': decodeSuccess(DailyPackageDownloads),
            orElse: unexpectedStatus,
          }),
        ),
      ),
    'GET/versions/{packageName}/last-week': packageName =>
      HttpClientRequest.get(`/versions/${packageName}/last-week`).pipe(
        withResponse(
          HttpClientResponse.matchStatus({
            '2xx': decodeSuccess(PackageVersionsDownloadCount),
            orElse: unexpectedStatus,
          }),
        ),
      ),
    'PUT/-/user/org.couchdb.user:{user}': (user, options) =>
      HttpClientRequest.put(`/-/user/org.couchdb.user:${user}`).pipe(
        HttpClientRequest.setHeaders({
          'npm-otp': options.params?.['npm-otp'] ?? undefined,
        }),
        HttpClientRequest.bodyUnsafeJson(options.payload),
        withResponse(
          HttpClientResponse.matchStatus({
            '2xx': decodeSuccess(LoginResponse),
            '401': decodeError('LegacyStatus', LegacyStatus),
            orElse: unexpectedStatus,
          }),
        ),
      ),
    'GET/-/npm/v1/user': () =>
      HttpClientRequest.get(`/-/npm/v1/user`).pipe(
        withResponse(
          HttpClientResponse.matchStatus({
            '2xx': decodeSuccess(Profile),
            orElse: unexpectedStatus,
          }),
        ),
      ),
    'POST/-/npm/v1/user': options =>
      HttpClientRequest.post(`/-/npm/v1/user`).pipe(
        HttpClientRequest.setHeaders({
          'npm-otp': options.params?.['npm-otp'] ?? undefined,
        }),
        HttpClientRequest.bodyUnsafeJson(options.payload),
        withResponse(
          HttpClientResponse.matchStatus({
            '2xx': decodeSuccess(Profile),
            orElse: unexpectedStatus,
          }),
        ),
      ),
    'GET/-/npm/v1/tokens': () =>
      HttpClientRequest.get(`/-/npm/v1/tokens`).pipe(
        withResponse(
          HttpClientResponse.matchStatus({
            '2xx': decodeSuccess(TokenPage),
            orElse: unexpectedStatus,
          }),
        ),
      ),
    'POST/-/npm/v1/tokens': options =>
      HttpClientRequest.post(`/-/npm/v1/tokens`).pipe(
        HttpClientRequest.setHeaders({
          'npm-otp': options.params?.['npm-otp'] ?? undefined,
        }),
        HttpClientRequest.bodyUnsafeJson(options.payload),
        withResponse(
          HttpClientResponse.matchStatus({
            '2xx': decodeSuccess(Token),
            orElse: unexpectedStatus,
          }),
        ),
      ),
    'DELETE/-/npm/v1/tokens/token/{uuid}': (uuid, options) =>
      HttpClientRequest.del(`/-/npm/v1/tokens/token/${uuid}`).pipe(
        HttpClientRequest.setHeaders({
          'npm-otp': options?.['npm-otp'] ?? undefined,
        }),
        withResponse(
          HttpClientResponse.matchStatus({
            '204': () => Effect.void,
            orElse: unexpectedStatus,
          }),
        ),
      ),
    'DELETE/-/user/token/{token}': (token, options) =>
      HttpClientRequest.del(`/-/user/token/${token}`).pipe(
        HttpClientRequest.setHeaders({
          'npm-otp': options?.['npm-otp'] ?? undefined,
        }),
        withResponse(
          HttpClientResponse.matchStatus({
            '2xx': decodeSuccess(LegacyStatus),
            orElse: unexpectedStatus,
          }),
        ),
      ),
    'GET/-/npm/v1/hooks': options =>
      HttpClientRequest.get(`/-/npm/v1/hooks`).pipe(
        HttpClientRequest.setUrlParams({
          package: options?.['package'] as any,
          limit: options?.['limit'] as any,
          offset: options?.['offset'] as any,
        }),
        withResponse(
          HttpClientResponse.matchStatus({
            '2xx': decodeSuccess(HookSearchResults),
            orElse: unexpectedStatus,
          }),
        ),
      ),
    'POST/-/npm/v1/hooks/hook': options =>
      HttpClientRequest.post(`/-/npm/v1/hooks/hook`).pipe(
        HttpClientRequest.setHeaders({
          'npm-otp': options.params?.['npm-otp'] ?? undefined,
        }),
        HttpClientRequest.bodyUnsafeJson(options.payload),
        withResponse(
          HttpClientResponse.matchStatus({
            '2xx': decodeSuccess(Hook),
            orElse: unexpectedStatus,
          }),
        ),
      ),
    'GET/-/npm/v1/hooks/hook/{id}': id =>
      HttpClientRequest.get(`/-/npm/v1/hooks/hook/${id}`).pipe(
        withResponse(
          HttpClientResponse.matchStatus({
            '2xx': decodeSuccess(Hook),
            orElse: unexpectedStatus,
          }),
        ),
      ),
    'PUT/-/npm/v1/hooks/hook/{id}': (id, options) =>
      HttpClientRequest.put(`/-/npm/v1/hooks/hook/${id}`).pipe(
        HttpClientRequest.setHeaders({
          'npm-otp': options.params?.['npm-otp'] ?? undefined,
        }),
        HttpClientRequest.bodyUnsafeJson(options.payload),
        withResponse(
          HttpClientResponse.matchStatus({
            '2xx': decodeSuccess(Hook),
            orElse: unexpectedStatus,
          }),
        ),
      ),
    'DELETE/-/npm/v1/hooks/hook/{id}': (id, options) =>
      HttpClientRequest.del(`/-/npm/v1/hooks/hook/${id}`).pipe(
        HttpClientRequest.setHeaders({
          'npm-otp': options?.['npm-otp'] ?? undefined,
        }),
        withResponse(
          HttpClientResponse.matchStatus({
            '2xx': decodeSuccess(Hook),
            orElse: unexpectedStatus,
          }),
        ),
      ),
  }
}

